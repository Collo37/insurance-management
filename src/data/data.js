const randomReg = () => {
  const alphabet = "ABCDEFGHJKLMNPQRSTUVWXYZ";
  const lettersArr = [
    "K",
    ["A", "B", "C", "D"][
      Math.floor(Math.random() * (["A", "B", "C", "D"].length - 1))
    ],
    alphabet[Math.floor(Math.random() * (alphabet.length - 1))],
  ];
  const numbers = "0123456789";
  const numbersArr = [
    numbers[Math.floor(Math.random() * (numbers.length - 1))],
    numbers[Math.floor(Math.random() * (numbers.length - 1))],
    numbers[Math.floor(Math.random() * (numbers.length - 1))],
    alphabet[Math.floor(Math.random() * (alphabet.length - 1))],
  ];

  const alphabetical = lettersArr.join("");
  const numerical = numbersArr.join("");
  const plate = alphabetical + " " + numerical;
  return plate;
};

