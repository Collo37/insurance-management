const generatePayments = (maxAmt = 10000, length = 20) => {
  const payments = [];

  for (let i = 0; i < length - 1; i++) {
    let paymentMode;
    if (i % 5 === 0) {
      paymentMode = "Mobile Money";
    } else if (i % 3 === 0) {
      paymentMode = "Banker's Cheque";
    } else if (i % 2 === 0) {
      paymentMode = "Credit";
    } else {
      paymentMode = "Cash";
    }
    payments.push({
      amount: Math.floor(Math.random() * maxAmt),
      paymentMode,
      transactionCode: i % 5 === 0 ? `MPESACODE${i}` : `NON-MPESA${i}`,
      transactionDate: new Date(Date.now()),
      vehicleId:
        vehicleIds[Math.floor(Math.random() * (vehicleIds.length - 1))],
      expiryDate: new Date(
        2023,
        Math.floor(Math.random() * 12),
        Math.floor(Math.random() * 31)
      ),
    });
  }
  return payments;
};

